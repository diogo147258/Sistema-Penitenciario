    private void btSalvarVisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarVisActionPerformed

        VisitaModel visita = new VisitaModel();
        String sexo = "";

        visita.setNomeVis(tfNomeVis.getText());
        visita.setTelefoneVis(tfTelefoneVis.getText());
        visita.setCpfVis(tfCpfVis.getText());
        visita.setDataNascimentoVis(tfDataNascimentoVis.getText());
        visita.setRgVis(tfRGVis.getText());

        if (rbMasculino.isSelected()) {
            sexo = rbMasculino.getText();
        } else {
            sexo = rbFeminino.getText();
        }

        visita.setSexoVis(sexo);

        visita.setNomeDoPreso(tfNomeDoPreso.getText());
        visita.setParentesco(tfParentescoVis.getText());
        visita.setDataVis(tfDataVis.getText());
        visita.setHoraVis(tfHoraVis.getText());
        visita.setMotivoVis(tfMotivoVis.getText());

        VisitaController visitaController = new VisitaController();
        visitaController.inserir(visita);
        limparCampos();

        tfNomeVis.setEnabled(false);
        tfTelefoneVis.setEnabled(false);
        tfCpfVis.setEnabled(false);
        tfDataNascimentoVis.setEnabled(false);
        tfRGVis.setEnabled(false);
        rbMasculino.setEnabled(false);
        rbFeminino.setEnabled(false);
        tfNomeDoPreso.setEnabled(false);
        tfParentescoVis.setEnabled(false);
        tfDataVis.setEnabled(false);
        tfHoraVis.setEnabled(false);
        tfMotivoVis.setEnabled(false);
        btSalvarVis.setEnabled(false);
        btLimparVis.setEnabled(false);
        btInserirVis.setEnabled(true);

    }//GEN-LAST:event_btSalvarVisActionPerformed

