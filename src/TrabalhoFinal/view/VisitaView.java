    int idVisitaAtual = 0;

    public void preencherCampos(VisitaModel visitante) {
        idVisitaAtual = visitante.getIdVisitante();
        String sexo = visitante.getSexoVis();
        tfNomeVis.setText(visitante.getNomeVis());
        tfTelefoneVis.setText(visitante.getTelefoneVis());
        tfCpfVis.setText(visitante.getCpfVis());
        tfRGVis.setText(visitante.getRgVis());
        tfDataNascimentoVis.setText(visitante.getDataNascimentoVis());
        if (sexo.equals("Masculino")) {
            rbMasculino.setSelected(true);
        } else if (sexo.equals("Feminino")) {
            rbFeminino.setSelected(true);
        }

        tfNomeDoPreso.setText(visitante.getNomeDoPreso());
        tfParentescoVis.setText(visitante.getParentesco());
        tfDataVis.setText(visitante.getDataVis());
        tfHoraVis.setText(visitante.getHoraVis());
        tfMotivoVis.setText(visitante.getMotivoVis());

    }

