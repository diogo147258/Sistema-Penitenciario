    public void listarVisita() {
        List<VisitaModel> listarVisita = new VisitaController().listar();
        DefaultTableModel modelo = (DefaultTableModel) JTVisita.getModel();
        modelo.setRowCount(0);
        for (VisitaModel visita : listarVisita) {
            modelo.addRow(new Object[]{
                visita.getIdVisitante(),
                visita.getNomeVis(),
                visita.getTelefoneVis(),
                visita.getCpfVis(),
                visita.getDataNascimentoVis(),
                visita.getRgVis(),
                visita.getSexoVis(),
                visita.getNomeDoPreso(),
                visita.getParentesco(),
                visita.getDataVis(),
                visita.getHoraVis(),
                visita.getMotivoVis(),});
        }
    }

