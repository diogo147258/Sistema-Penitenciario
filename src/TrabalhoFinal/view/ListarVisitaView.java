    private void btEditarVisitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarVisitaActionPerformed
        int selecionada = JTVisita.getSelectedRow();
        if (selecionada != -1) {
            VisitaModel visitaModel = new VisitaModel();

            visitaModel.setIdVisitante(Integer.parseInt(JTVisita.getValueAt(selecionada, 0).toString()));
            visitaModel.setNomeVis(JTVisita.getValueAt(selecionada, 1).toString());
            visitaModel.setTelefoneVis(JTVisita.getValueAt(selecionada, 2).toString());
            visitaModel.setCpfVis(JTVisita.getValueAt(selecionada, 3).toString());
            visitaModel.setDataNascimentoVis(JTVisita.getValueAt(selecionada, 4).toString());
            visitaModel.setRgVis(JTVisita.getValueAt(selecionada, 5).toString());
            visitaModel.setSexoVis(JTVisita.getValueAt(selecionada, 6).toString());
            visitaModel.setNomeDoPreso(JTVisita.getValueAt(selecionada, 7).toString());
            visitaModel.setParentesco(JTVisita.getValueAt(selecionada, 8).toString());
            visitaModel.setDataVis(JTVisita.getValueAt(selecionada, 9).toString());
            visitaModel.setHoraVis(JTVisita.getValueAt(selecionada, 10).toString());
            visitaModel.setMotivoVis(JTVisita.getValueAt(selecionada, 11).toString());

            VisitaView visitaView = new VisitaView();
            visitaView.ativarSalvar();
            JDesktopPane desktop = this.getDesktopPane();
            desktop.add(visitaView);
            visitaView.setVisible(true);
            visitaView.preencherCampos(visitaModel);
        }
    }//GEN-LAST:event_btEditarVisitaActionPerformed

    private void btExcluirVisitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirVisitaActionPerformed
        int selecionada = JTVisita.getSelectedRow();
        if (selecionada != -1) {
            VisitaModel visitaModel = new VisitaModel();
            VisitaController visitaController = new VisitaController();
            visitaModel.setIdVisitante((Integer) JTVisita.getValueAt(selecionada, 0));
            visitaController.excluir(visitaModel);
            listarVisita();
        }
    }//GEN-LAST:event_btExcluirVisitaActionPerformed

